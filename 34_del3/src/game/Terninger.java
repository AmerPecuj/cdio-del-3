package game;

public class Terninger {
	
	// VARIABLE -------------------------------------------------------
	private int Terning = 0;
	private final int MAX = 6;
	// ----------------------------------------------------------------
	
	
	// KONSTRUKTOER ---------------------------------------------------
	public Terninger() {
		this.Terning = 0;
	}
	// ----------------------------------------------------------------

	// METODER --------------------------------------------------------
	public void kastTerninger() {
		Terning = (int) (Math.random() * MAX) + 1;
	}
	// ----------------------------------------------------------------
	
	// GETTER-METODER -------------------------------------------------
	public int getTerning1() {
		return Terning;
	}
	// ----------------------------------------------------------------
	
	// SETTER-METODER -------------------------------------------------
	public void setTerning1(int n) {
		Terning = n;
	}
	// ----------------------------------------------------------------
}