package game;

public class Terninger {
	
	// VARIABLE -------------------------------------------------------
	private int Terning = 0;
	private final int MAX = 6;
	// ----------------------------------------------------------------
	
	
	// KONSTRUKTOER ---------------------------------------------------
	protected Terninger() {
		this.Terning = 0;
	}
	// ----------------------------------------------------------------

	// METODER --------------------------------------------------------
	protected void kastTerning() {
		Terning = (int) (Math.random() * MAX) + 1;
	}
	// ----------------------------------------------------------------
	
	// GETTER-METODER -------------------------------------------------
	protected int getTerning() {
		return Terning;
	}
	// ----------------------------------------------------------------
	
	// SETTER-METODER -------------------------------------------------
	protected void setTerning(int n) {
		Terning = n;
	}
	// ----------------------------------------------------------------
}